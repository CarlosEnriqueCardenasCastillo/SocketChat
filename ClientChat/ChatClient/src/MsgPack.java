
public class MsgPack {
    private String ContactName;
    private String ContactIP;
    private String ContactMsg;
    public void setContactName(String ContactName){
        this.ContactName=ContactName;
    }
    public String getContactName(){
        return this.ContactName;
    }
    public void setContactIP(String ContactIP){
        this.ContactIP=ContactIP;
    }
    public String getContactIP(){
        return this.ContactIP;
    }
    public void setContactMsg(String ContactMsg){
        this.ContactMsg=ContactMsg;
    }
    public String getContactMsg(){
        return this.ContactMsg;
    }
}
